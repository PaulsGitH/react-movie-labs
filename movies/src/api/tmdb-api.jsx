export const getCompany = ({ queryKey }) => {
  const [, { id }] = queryKey;
  return fetch(
    `https://api.themoviedb.org/3/company/${id}?api_key=${import.meta.env.VITE_TMDB_KEY}`
  )
    .then(async (res) => {
      if (!res.ok) {
        const err = await res.json().catch(() => ({}));
        throw new Error(err.status_message || `Company ${id} not found`);
      }
      return res.json();
    })
    .catch((e) => {
      throw e;
    });
};


